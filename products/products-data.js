// Helper functions
TaaiboschProducts.getAllProducts = function() {
    return [
        ...this.seaMossGels,
        ...this.seaMossGummies,
        ...this.rawHerbs,
        ...this.capsules
    ];
};

TaaiboschProducts.getProductsByCategory = function(category) {
    return this.getAllProducts().filter(product => product.category === category);
};

TaaiboschProducts.getProductById = function(id) {
    return this.getAllProducts().find(product => product.id === id);
};

TaaiboschProducts.searchProducts = function(query) {
    const searchTerm = query.toLowerCase();
    return this.getAllProducts().filter(product => 
        product.name.toLowerCase().includes(searchTerm) ||
        product.description.toLowerCase().includes(searchTerm) ||
        product.benefits.some(benefit => benefit.toLowerCase().includes(searchTerm))
    );
};

TaaiboschProducts.getFeaturedProducts = function() {
    return this.getAllProducts().filter(product => product.badge === 'best-seller').slice(0, 6);
};

TaaiboschProducts.getNewProducts = function() {
    return this.getAllProducts().filter(product => product.badge === 'new');
};

// Export for use in other modules
